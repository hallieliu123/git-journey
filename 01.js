// 3. git add < filename > 添加此更改文件代码  || git add. 添加所有更改文件代码

// 4. git commit -m 'memo' 暂存区提交至版本库

// 5. git commit -a -m 'memo' 工作区同时更新到暂存区和版本库

// 6. git log 查看提交记录

// 7. git reset HEAD < filename > 从暂存区撤回

